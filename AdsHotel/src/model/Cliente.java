package model;

/**
 *
 * @author alexr
 */
public class Cliente {
    private String nome;
    private String cpf;
    private String email;
    private String telefone;

    public String getNome() {
        return nome;
    }

    public void setCliente(String nome, String cpf, String email, String telefone) {
        this.nome = nome;
        this.cpf = cpf;
        this.email = email;
        this.telefone = telefone;
    }

    public String getCpf() {
        return cpf;
    }

    public String getEmail() {
        return email;
    }

    public String getTelefone() {
        return telefone;
    }
}
